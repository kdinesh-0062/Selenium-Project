package FirstSelenium;
public class SquareNumbers {
    public static void main(String[] args) {
        // Loop through numbers 1 to 5
        for (int i = 1; i <= 5; i++) {
            // Calculate the square using multiplication
            int square = i * i;
            // Print the square
            System.out.println("The square of " + i + " is: " + square);
        }
    }
}
